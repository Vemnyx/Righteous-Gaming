// CardByCardIdentifier returns the card with the given card_identifier, or ErrCardNotFound.
func (r *Repository) CardByCardIdentifier(ctx context.Context, identifier string) (*Card, error) {
	ident := strings.TrimSpace(identifier)
	if ident == "" {
		return nil, ErrCardNotFound
	}
	if r.pool == nil {
		return nil, fmt.Errorf("repository: pool is closed")
	}
	q := cardSelectJoinSet + ` WHERE c.card_identifier = $1`
	row := r.pool.QueryRow(ctx, q, ident)
	c, err := scanCardWithSetName(row)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, ErrCardNotFound
		}
		return nil, fmt.Errorf("repository: card by card_identifier: %w", err)
	}
	return c, nil
}

// CardsBySetID returns all cards for a set, ordered by set_num then id.
func (r *Repository) CardsBySetID(ctx context.Context, setID int) ([]Card, error) {
	if r.pool == nil {
		return nil, fmt.Errorf("repository: pool is closed")
	}
	q := `
SELECT ` + cardSelectColumns + `
FROM cards c
` + cardPrintingLateralJoin + `
WHERE c.set_id = $1
ORDER BY cp.set_num ASC, c.id ASC`
	rows, err := r.pool.Query(ctx, q, setID)
	if err != nil {
		return nil, fmt.Errorf("repository: cards by set id: %w", err)
	}
	defer rows.Close()

	out := make([]Card, 0, 64)
	for rows.Next() {
		c, err := scanCard(rows)
		if err != nil {
			return nil, fmt.Errorf("repository: cards by set id scan: %w", err)
		}
		out = append(out, *c)
	}
	if err := rows.Err(); err != nil {
		return nil, fmt.Errorf("repository: cards by set id rows: %w", err)
	}
	return out, nil
}

// ListCards returns every card with set display name, ordered by set_code, set_num, id.
func (r *Repository) ListCards(ctx context.Context) ([]Card, error) {
	if r.pool == nil {
		return nil, fmt.Errorf("repository: pool is closed")
	}
	q := `
SELECT ` + cardSelectColumnsFromC + `, s.name AS set_name
FROM cards c
` + cardPrintingLateralJoin + `
INNER JOIN sets s ON s.id = c.set_id
ORDER BY cp.set_code ASC, cp.set_num ASC, c.id ASC`
	rows, err := r.pool.Query(ctx, q)
	if err != nil {
		return nil, fmt.Errorf("repository: list cards: %w", err)
	}
	defer rows.Close()

	out := make([]Card, 0, 256)
	for rows.Next() {
		c, err := scanCardWithSetName(rows)
		if err != nil {
			return nil, fmt.Errorf("repository: list cards scan: %w", err)
		}
		out = append(out, *c)
	}
	if err := rows.Err(); err != nil {
		return nil, fmt.Errorf("repository: list cards rows: %w", err)
	}
	return out, nil
}
