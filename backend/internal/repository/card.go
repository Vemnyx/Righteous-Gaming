// ErrCardNotFound is returned when no row matches the requested card identifier.
var ErrCardNotFound = errors.New("repository: card not found")

// Card is a row from cards joined to its primary printing (lowest card_printings.id).
type Card struct {
	ID              int
	SetID           int
	Name            string
	CardIdentifier  *string
	ImageURL        *string
	FunctionalText  *string
	Rarity          *int16
	SetCode         string
	SetNum          int16
	Type            int16
	Subtypes        []int16
	Classes         []int16
	Hybrid          bool
	Talents         []int16
	Pitch           *int16
	Cost            *int16
	Power           *int16
	Block           *int16
	Heroes          []int16
	Life            *int16
	Intellect       *int16
	Keywords        []int16
	Formats         []int16
	Specializations []int16
	Fusions         []int16
	// SetName is set when listing with a join to sets (ListCards); otherwise empty.
	SetName string
	// Printings is populated when AttachPrintings is called before API serialization.
	Printings []CardPrinting
}

// CreateCardPrintingInput holds one row for card_printings on insert.
type CreateCardPrintingInput struct {
	SetCode  string
	SetNum   int16
	Rarity   *int16
	ImageURL *string
}

// CreateCardInput holds fields to insert a card and its printings (card id is generated by the DB).
// Nil slices and pointers persist as NULL where the column is nullable.
type CreateCardInput struct {
	SetID           int
	Name            string
	CardIdentifier  *string
	ImageURL        *string
	FunctionalText  *string
	Rarity          *int16
	SetCode         string
	SetNum          int16
	Type            int16
	Subtypes        []int16
	Classes         []int16
	Hybrid          bool
	Talents         []int16
	Pitch           *int16
	Cost            *int16
	Power           *int16
	Block           *int16
	Heroes          []int16
	Life            *int16
	Intellect       *int16
	Keywords        []int16
	Formats         []int16
	Specializations []int16
	Fusions         []int16
	Printings       []CreateCardPrintingInput
}

// Primary printing = lowest card_printings.id per card (matches migrated 1:1 rows).
const cardPrintingLateralJoin = `
INNER JOIN LATERAL (
	SELECT cp.image_url, cp.rarity, cp.set_code, cp.set_num
	FROM card_printings cp
	WHERE cp.card_id = c.id
	ORDER BY cp.id ASC
	LIMIT 1
) cp ON true`

const cardSelectColumns = `
c.id, c.set_id, c.name, c.card_identifier, cp.image_url, c.functional_text, cp.rarity, cp.set_code, cp.set_num, c.type,
c.subtypes, c.classes, c.hybrid, c.talents, c.pitch, c.cost, c.power, c.block, c.heroes, c.life, c.intellect,
c.keywords, c.formats, c.specializations, c.fusions`

// Qualified columns for JOIN queries (cards aliased as c).
const cardSelectColumnsFromC = cardSelectColumns

// Used when GROUP BY c.id also selects primary printing columns.
const cardPrintingGroupBy = `c.id, cp.image_url, cp.rarity, cp.set_code, cp.set_num`

