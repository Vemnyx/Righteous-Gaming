// CreateCard inserts a card and returns the persisted row.
func (r *Repository) CreateCard(ctx context.Context, in CreateCardInput) (*Card, error) {
	if r.pool == nil {
		return nil, fmt.Errorf("repository: pool is closed")
	}
	c, err := insertCardReturning(ctx, r.pool, in)
	if err != nil {
		return nil, fmt.Errorf("repository: create card: %w", err)
	}
	return c, nil
}

// CreateCardsBatch inserts cards in one transaction (all-or-nothing).
func (r *Repository) CreateCardsBatch(ctx context.Context, inputs []CreateCardInput) ([]Card, error) {
	if len(inputs) == 0 {
		return nil, nil
	}
	if r.pool == nil {
		return nil, fmt.Errorf("repository: pool is closed")
	}
	tx, err := r.pool.Begin(ctx)
	if err != nil {
		return nil, fmt.Errorf("repository: batch begin: %w", err)
	}
	defer tx.Rollback(ctx)

	out := make([]Card, 0, len(inputs))
	for i := range inputs {
		c, err := insertCardReturning(ctx, tx, inputs[i])
		if err != nil {
			return nil, fmt.Errorf("repository: batch item %d: %w", i, err)
		}
		out = append(out, *c)
	}
	if err := tx.Commit(ctx); err != nil {
		return nil, fmt.Errorf("repository: batch commit: %w", err)
	}
	return out, nil
}

// cardSelectJoinSet is cards plus set display name (set_name), for single-card reads and lists.
const cardSelectJoinSet = `
SELECT ` + cardSelectColumnsFromC + `, s.name AS set_name
FROM cards c
` + cardPrintingLateralJoin + `
INNER JOIN sets s ON s.id = c.set_id
`

// CardByID returns the card with the given id, or ErrCardNotFound.
func (r *Repository) CardByID(ctx context.Context, id int) (*Card, error) {
	if r.pool == nil {
		return nil, fmt.Errorf("repository: pool is closed")
	}
	q := cardSelectJoinSet + ` WHERE c.id = $1`
	row := r.pool.QueryRow(ctx, q, id)
	c, err := scanCardWithSetName(row)
	if err != nil {
		if errors.Is(err, pgx.ErrNoRows) {
			return nil, ErrCardNotFound
		}
		return nil, fmt.Errorf("repository: card by id: %w", err)
	}
	return c, nil
}

