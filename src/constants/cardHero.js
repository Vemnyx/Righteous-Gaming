/**
 * @param {number} id
 * @returns {string | undefined}
 */
export function cardHeroName(id) {
  return CARD_HERO_NAMES[id];
}

/**
 * @param {number} id
 * @returns {boolean}
 */
export function isValidCardHeroId(id) {
  return Number.isInteger(id) && id >= 0 && id < CARD_HERO_NAMES.length;
}
